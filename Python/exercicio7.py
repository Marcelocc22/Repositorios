# Area  

larg = float(input('Qual é a Largura da parede: '))
alt = float(input('Qual é a altura da parede: '))
area = larg * alt
print('Sua parede tem dimensões de {}m X {}m e a área mede {}m²'.format(larg, alt, area))
tinta = area / 2
print('Para pintar essa parede será necessário {}L de tinta!'.format(tinta))



